#Printing menu
def print_menu(trivia_sets):
    print("\nChoose the movie that you'd like to quiz your self on, or check your highscores!")
    for key, data in trivia_sets.items():
        print(f"\n{key}.) {data['title']}")
    print("\n7.) Show Highscores")


#Selecting movie
def select_movie(trivia_sets):
    while True:
        choice = input("\nEnter your choice:").strip()
        if choice in trivia_sets:
            return choice
        elif choice == "7":
            show_highscore()
            continue

        print("\nInvalid choice, please try again.")




#Saving highscore
def save_highscore(movie_title, score):
    with open("highscores.txt", "a") as file:
         file.write(f"{movie_title}: {score}%\n")



#Showing highscore
def show_highscore():
    try:
        with open("highscores.txt", "r") as file:
            print(" ---------------------------")
            print(" |       HIGH SCORE        |")
            print(" ---------------------------")
            print(file.read())
    except FileNotFoundError:
        print("\nNo highscores recorded.")




#Running quiz
def run_quiz(trivia):
    score = 0
    movie_title = trivia["title"]
    question_num = 0
    questions = trivia["questions"]
    options = trivia["options"]
    answers = trivia["answers"]

    print(f"\n{movie_title}")
    for question in questions:
        print("------------------------------------------------------------------------------")
        print(question)
        for option in options[question_num]:
            print(option)

        while True:
            guess = input("\nEnter your answer:").strip().lower()
            if guess in ('a', 'b', 'c', 'd'):
                break
            print("\nInvalid choice, please try again.")

        if guess == answers[question_num]:
            score += 1
            print(f"Correct! Your current score is {score}.")
        else:
            print("Incorrect!")

        question_num += 1

    final_score = (score / len(questions) * 100)
    print("\n")
    print(" ---------------------------")
    print(" |      FINAL RESULTS      |")
    print(" ---------------------------")

    if final_score == 100:
        print(f"\nYour final score is {final_score}%! You passed with an A+!")
    elif final_score >= 90:
        print(f"\nYour final score is {final_score}%! You passed with an A!")
    elif final_score >= 80:
        print(f"\nYour final score is {final_score}%! You passed with a B!")
    elif final_score >= 70:
        print(f"\nYour final score is {final_score}%! You passed with a C!")
    elif final_score >= 60:
        print(f"\nYour final score is {final_score}%! You just BARELY passed with a D! That was a close one!")
    else:
        print(f"\nYour final score is {final_score}%! You failed... Try again young padawan.")

    save_highscore(movie_title, final_score)



#Play again
def play_again():
    while True:
        answer = input("\nWould you like to test your knowledge on a different movie, or check your highscores? (yes/no):").strip().lower()
        if answer == "yes":
            return True
        elif answer == "no":
            return False
        else:
            print("\nInvalid choice, please try again.")





def main(trivia_sets):
    print("______________________________")
    print("    STAR WARS TRIVIA NIGHT    ")
    print("______________________________")

    while True:
        print_menu(trivia_sets)
        choice = select_movie(trivia_sets)
        run_quiz(trivia_sets[choice])

        if not play_again():
            print("\nMay the Force be with you.")
            break


if __name__ == "__main__":
    main(trivia_sets)